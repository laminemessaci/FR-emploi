export const colorExtension = extension => {
  switch (extension) {
    case 'pdf':
      return '#dc0f0f'
    case 'xml':
      return '#e6900d'
    case 'ppt':
      return '#e6900d'
    case 'pptx':
      return '#e6900d'
    case 'txt':
      return '#EDC70B'
    case 'rtf':
      return '#EDC70B'
    case 'csv':
      return '#EDC70B'
    case 'jpg':
      return '#e96ebd'
    case 'jpeg':
      return '#e96ebd'
    case 'xlsm':
      return '#619600'
    case 'xltm':
      return '#619600'
    case 'xltx':
      return '#619600'
    case 'dat':
      return '#619600'
    case 'tif':
      return '#619600'
    case 'tiff':
      return '#619600'
    case 'xls':
      return '#619600'
    case 'xlsx':
      return '#619600'
    case 'html':
      return '#427fda'
    case 'msg':
      return '#427fda'
    case 'odt':
      return '#427fda'
    case 'doc':
      return '#427fda'
    case 'docx':
      return '#427fda'
    case 'png':
      return '#427fda'
    default:
      return '#427fda'
  }
}
